# Aritméticos
print(17 // 5)      # divisão inteira R: 3
print(17 % 5)       # resto R: 2
print(2 ** 10)      # potência R: 1024
print(10 / 3)       # divisão real R: 3.333

# Comparação (True ou False?)
print(10 > 5 and 3 < 2) # R: False
print(10 > 5 or 3 < 2)  # R: True
print(not True)          # R: False
print(5 == 5.0)         # R: True
print("abc" == "ABC")    # R: False
print(10 != "10")        # R: True